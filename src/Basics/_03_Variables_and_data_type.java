package Basics;

public class _03_Variables_and_data_type {
    public static void main(String[] args){
        // Data types in Java
        /*
        1. primitive data type:
            int, long, double, short, byte, float, char, bool

        2. non-primitive data type(derived by primitive data types):
            array, list, linked list, stack, queue

         Variables: it is a type of container which is store a value, and it can be changed during the execution
         */
        int number = 10;
        short myShort = 90;
        long myLong = 1000;
        double myDouble = 23.23;
        float myFloat = 34.23f;
        char myChar = 'y';
        boolean myBoolean = false;
        byte myByte = 4;
        System.out.println(number);
        System.out.println(myShort);
        System.out.println(myDouble);
        System.out.println(myFloat);
        System.out.println(myChar);
        System.out.println(myBoolean);
        System.out.println(myByte);

    }
}
